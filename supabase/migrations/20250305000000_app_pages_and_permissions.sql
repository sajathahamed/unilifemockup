-- Run this in Supabase SQL Editor or via supabase db push if using CLI.

-- Table: app_pages (all navigable pages with unique id)
CREATE TABLE IF NOT EXISTS app_pages (
  id smallint PRIMARY KEY,
  path text NOT NULL UNIQUE,
  label text NOT NULL,
  role text NOT NULL CHECK (role IN ('student','lecturer','admin','vendor','delivery','super_admin')),
  icon text NOT NULL,
  sort_order smallint NOT NULL DEFAULT 0
);

-- Table: role_page_permissions (super_admin can enable/disable pages per role)
CREATE TABLE IF NOT EXISTS role_page_permissions (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  role text NOT NULL CHECK (role IN ('student','lecturer','admin','vendor','delivery','super_admin')),
  page_id smallint NOT NULL REFERENCES app_pages(id) ON DELETE CASCADE,
  enabled boolean NOT NULL DEFAULT true,
  UNIQUE(role, page_id)
);

CREATE INDEX IF NOT EXISTS idx_role_page_permissions_role ON role_page_permissions(role);
CREATE INDEX IF NOT EXISTS idx_role_page_permissions_page_id ON role_page_permissions(page_id);

