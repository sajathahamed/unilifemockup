-- RLS (optional): allow read for authenticated, write only for super_admin (enforced in app)
ALTER TABLE app_pages ENABLE ROW LEVEL SECURITY;
ALTER TABLE role_page_permissions ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Allow read app_pages for authenticated" ON app_pages FOR SELECT TO authenticated USING (true);
CREATE POLICY "Allow read role_page_permissions for authenticated" ON role_page_permissions FOR SELECT TO authenticated USING (true);
CREATE POLICY "Allow insert role_page_permissions for authenticated" ON role_page_permissions FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "Allow update role_page_permissions for authenticated" ON role_page_permissions FOR UPDATE TO authenticated USING (true) WITH CHECK (true);
CREATE POLICY "Allow all app_pages for service role" ON app_pages FOR ALL TO service_role USING (true) WITH CHECK (true);
CREATE POLICY "Allow all role_page_permissions for service role" ON role_page_permissions FOR ALL TO service_role USING (true) WITH CHECK (true);
